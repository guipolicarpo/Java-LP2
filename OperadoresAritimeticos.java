public class OperadoresAritimeticos {
	
	public static void main (String[] args) {
		int a=5, b=2;
		double c;
		
		System.out.println("A = " + a);
		System.out.println("B = " + b);
		
		c= a+b;	
		System.out.println("A + B = " + c);
		
		c= a-b;	
		System.out.println("A - B = " + c);
		
		c= a*b;	
		System.out.println("A * B = " + c);	
		
		c= (double) a/b;	
		System.out.println("A / B = " + c);
		
		c= a%b;	
		System.out.println("A % B = " + c);			
	}
}

